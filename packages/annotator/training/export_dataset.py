#!/usr/bin/env python3
"""Convert the annotator's labels into an Ultralytics YOLO-OBB dataset.

Two sources, two roles:
  - data/synthetic/  -> train/val   (perspective-correct quads, already normalized)
  - data/annotations/ + images/ -> test   (the hand-labeled real photos; the
    synthetic->real gap is measured against these)

YOLO-OBB label format is one line per box:
    class_id x1 y1 x2 y2 x3 y3 x4 y4      (all normalized to [0,1])
which is exactly our 4-corner quad, so the synthetic conversion is a passthrough.

Images are symlinked (not copied) to avoid duplicating gigabytes. Output:
    data/yolo/
      images/{train,val,test}/*.jpg   (symlinks)
      labels/{train,val,test}/*.txt
      dataset.yaml

Usage:
    python training/export_dataset.py [--val-frac 0.1] [--seed 0]
"""
import argparse
import json
import math
import os
import random
import shutil
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent  # packages/annotator
SYN_IMG = ROOT / "data" / "synthetic" / "images"
SYN_ANN = ROOT / "data" / "synthetic" / "annotations"
REAL_IMG = ROOT / "images"
REAL_ANN = ROOT / "data" / "annotations"
OUT = ROOT / "data" / "yolo"
CLASS_ID = 0


def clip01(v):
    return 0.0 if v < 0 else 1.0 if v > 1 else v


def synthetic_quads(ann):
    """Synthetic boxes already carry normalized quads."""
    return [b["quad"] for b in ann.get("boxes", []) if b.get("quad") and len(b["quad"]) == 4]


def real_quads(ann):
    """Real boxes are oriented rects (cx, cy, angle + shared scale); build quads."""
    W, H = ann["width"], ann["height"]
    aspect = ann.get("aspect", 12.5)
    scale = ann.get("scale", 0.18)
    wpx = scale * W
    hpx = wpx / aspect
    corners = [(-wpx / 2, -hpx / 2), (wpx / 2, -hpx / 2), (wpx / 2, hpx / 2), (-wpx / 2, hpx / 2)]
    quads = []
    for b in ann.get("boxes", []):
        cx, cy = b["cx"] * W, b["cy"] * H
        a = math.radians(b["angle"])
        ca, sa = math.cos(a), math.sin(a)
        quads.append([[(cx + lx * ca - ly * sa) / W, (cy + lx * sa + ly * ca) / H] for lx, ly in corners])
    return quads


def label_lines(quads):
    lines = []
    for q in quads:
        coords = " ".join(f"{clip01(x):.6f} {clip01(y):.6f}" for x, y in q)
        lines.append(f"{CLASS_ID} {coords}")
    return lines


def write_example(img_path, quads, split):
    """Symlink the image and write its label file into the split."""
    img_dst = OUT / "images" / split / img_path.name
    lbl_dst = OUT / "labels" / split / (img_path.stem + ".txt")
    if img_dst.exists() or img_dst.is_symlink():
        img_dst.unlink()
    os.symlink(img_path.resolve(), img_dst)
    lbl_dst.write_text("\n".join(label_lines(quads)) + ("\n" if quads else ""))


def reset_dirs():
    if OUT.exists():
        shutil.rmtree(OUT)
    for split in ("train", "val", "test"):
        (OUT / "images" / split).mkdir(parents=True, exist_ok=True)
        (OUT / "labels" / split).mkdir(parents=True, exist_ok=True)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--val-frac", type=float, default=0.1)
    ap.add_argument("--seed", type=int, default=0)
    ap.add_argument("--limit", type=int, default=0, help="cap synthetic images used (0 = all); handy for fast baselines")
    args = ap.parse_args()

    reset_dirs()
    counts = {"train": 0, "val": 0, "test": 0}

    # Synthetic -> train/val
    syn = sorted(SYN_ANN.glob("*.json")) if SYN_ANN.exists() else []
    rng = random.Random(args.seed)
    if args.limit and len(syn) > args.limit:
        syn = rng.sample(syn, args.limit)
    for ann_path in syn:
        ann = json.loads(ann_path.read_text())
        img_path = SYN_IMG / ann["image"]
        if not img_path.exists():
            continue
        split = "val" if rng.random() < args.val_frac else "train"
        write_example(img_path, synthetic_quads(ann), split)
        counts[split] += 1

    # Real hand labels -> test
    real = sorted(REAL_ANN.glob("*.json")) if REAL_ANN.exists() else []
    for ann_path in real:
        ann = json.loads(ann_path.read_text())
        img_path = REAL_IMG / ann["image"]
        if not img_path.exists():
            continue
        write_example(img_path, real_quads(ann), "test")
        counts["test"] += 1

    yaml = OUT / "dataset.yaml"
    yaml.write_text(
        f"# Auto-generated by training/export_dataset.py\n"
        f"path: {OUT.resolve()}\n"
        f"train: images/train\n"
        f"val: images/val\n"
        f"test: images/test\n"
        f"names:\n  0: namebar\n"
    )
    print(f"train={counts['train']} val={counts['val']} test(real)={counts['test']}")
    print(f"dataset.yaml -> {yaml}")


if __name__ == "__main__":
    main()
